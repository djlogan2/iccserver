/**
 * @return {string}
 */
export const LEVEL2STRING = function (arg) {
    if (!Array.isArray(arg))
        arg = [arg];
    arg.sort(function (a, b) {
        return a - b;
    });

    let max = arg[arg.length - 1];
    let y = 0;
    let str = '';

    for (let x = 0; x <= max; x++) {
        if (y < arg.length && x === arg[y]) {
            str += '1';
            y++;
        } else
            str += '0';
    }

    return str;
};
